class City {
  final String? name;
  final String? country;
  final String? countryCode;
  final String? region;
  final String? regionCode;
  final double? latitude;
  final double? longitude;

  City({
    this.name,
    this.country,
    this.countryCode,
    this.region,
    this.regionCode,
    this.latitude,
    this.longitude
  });
}